"""
Python code to perform mathematical operation on two variable
"""

def Summation(num1: int, num2: int) -> int:
    """
    Calculate summation of two number
    """
    return num1 + num2

def Subtraction(num1: int, num2: int) -> int:
    """
    Calculate subtraction of two number
    """
    return num1 - num2

def Multiplication(num1: int, num2: int) -> int:
    """
    Calculate multiplication of two number
    """
    return num1 * num2

def Division(num1: int, num2: int) -> float:
    """
    Calculate division of two number
    """
    return num1 / num2




